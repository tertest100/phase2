package com.simplilearn.loops;

public class TestWhileLoop {

	public static void main(String[] args) {
		
		// Simple Counter
		
		//initialization 
		int count = 100;
		
		// condition 
		while(count<10) {
			// while with always true is infinity loop.
			System.out.println("The Count Value :"+count);
			
			//increment
			count ++;			
		}

	}

}
